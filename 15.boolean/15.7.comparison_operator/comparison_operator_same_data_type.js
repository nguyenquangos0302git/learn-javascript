// string compartor
"a" > "b"; // false
"aa" <= "b"; // true (longer doesn't mean will be greater)
"abc" > "abd"; // false (because c is less than d)
"a" > "A"; // true
"A" > "9"; // true
"easy" == "easy"; // true
"easy frontend" != "easy"; // true
"easy frontend" > "easy"; // true

// boolean compartor
true > false; // true
true == true; // true
true == false; // false
true != false; // true
